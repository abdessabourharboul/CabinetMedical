    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        try {
            Paiement p1 = paiementHelper.getSelected();
            Paiement paiement = getParam();
            paiement.setId(p1.getId());
            paiement.setConsultation(p1.getConsultation());
            int res = paiementService.update(paiement);
            if (res >= 0) {
                paiementHelper.update(paiement);
                viderChamps();
                JOptionPane.showMessageDialog(null, "Le paiement est modifié avec succès", "Information", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Le paiement n'est pas modifié", "Eror", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(PaiementView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Le rendez-Vous n'est pas modifié", "Eror", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton18ActionPerformed

    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        Paiement paiement = paiementHelper.getSelected();
        try {

            int res = paiementService.delete(paiement);
            if (res > 0) {
                paiementHelper.remove(paiement);
                viderChamps();
                JOptionPane.showMessageDialog(null, "Le rendez-Vous est suprimé avec succès", "Information", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Le rendez-Vous n'est pas supprimé", "Eror", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(PaiementView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erreur", "Eror", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton20ActionPerformed

