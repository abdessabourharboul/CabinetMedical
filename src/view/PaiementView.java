    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:

        Paiement paiement = getParam();
        try {
            paiement.setConsultation(consultationHelper.getSelected());
            int res = paiementService.save(paiement);
            if (res > 0) {
                paiementHelper = new PaiementHelper(jTable3, paiementService.findAll());
                viderChamps();
                JOptionPane.showMessageDialog(null, "Le paiement est Ajouté avec Succès", "Information", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Le paiement n'est pas Ajouté", "Eror", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(PaiementView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Le paiement n'est pas Ajouté", "Eror", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton17ActionPerformed

